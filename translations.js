// Idioma padrão
let currentLanguage = 'pt';

// Função para alterar o idioma
function setLanguage(language) {
    if (translations[language]) {
        currentLanguage = language;
        updatePageContent();
    }
}

// Função para obter uma string traduzida
function t(key) {
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
        return translations[currentLanguage][key];
    }
    return translations['en'][key] || key;
}

// Função para atualizar o conteúdo da página
function updatePageContent() {
    // Atualizar textos na UI
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (key) {
            element.textContent = t(key);
        }
    });
    
    // Atualizar textos específicos para cada linguagem
    document.querySelector('title').textContent = t('app_title');
    
    // Outros elementos específicos podem ser atualizados aqui
}

// Inicializar com o idioma padrão quando o documento estiver pronto
document.addEventListener('DOMContentLoaded', function() {
    updatePageContent();
});